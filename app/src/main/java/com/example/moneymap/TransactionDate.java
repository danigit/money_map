package com.example.moneymap;

public class TransactionDate {
    public String day;
    public String dayNumber;
    public String month;
    public String year;

    public TransactionDate(){}

    public TransactionDate(String day, String dayNumber, String month, String year){
        this.day = day;
        this.dayNumber = dayNumber;
        this.month = month;
        this.year = year;
    }
}
